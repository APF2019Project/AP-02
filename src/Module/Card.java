package Module;

public class Card {
    private String name;
    private int hp;
    private int price;
    private int damage;

    public String getName() {
        return name;
    }

    public int getHp() {
        return hp;
    }

    public int getPrice() {
        return price;
    }

    public int getDamage() {
        return damage;
    }
}
